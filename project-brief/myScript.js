function displayInfo(id) {
  if (id === "") {
    document.getElementById("popup-1").classList.toggle("active");
  } else {
    document.getElementById("popup-1").classList.toggle("active");
    document.getElementById("popup-1").style.display = "block";

    let header = document.getElementById("head-tag");
    let country = document.getElementById("country");
    let skills = document.getElementById("skills");
    header.innerText = persons[id].name;

    country.innerText = "COUNTRY - " + persons[id].country;
    skills.innerText = "SKILLS - " + persons[id].skills;
  }
}

// //filter by gender code

function filterBy(gender) {
  if (gender === "f") {
    // hide all male by adding the hide class
    const maleProfiles = document.querySelectorAll(".male");
    maleProfiles.forEach((node) => {
      node.classList.add("hide");
    });
    // make sure hide class is removed from all females
    const femaleProfiles = document.querySelectorAll(".female");
    femaleProfiles.forEach((node) => {
      console.log(node.classList);
      node.classList.remove("hide");
    });
  } else if (gender === "m") {
    const femaleProfiles = document.querySelectorAll(".female");
    femaleProfiles.forEach((node) => {
      node.classList.add("hide");
    });

    const maleProfiles = document.querySelectorAll(".male");
    maleProfiles.forEach((node) => {
      node.classList.remove("hide");
    });
  } else {
    const femaleProfiles = document.querySelectorAll(".female");
    femaleProfiles.forEach((node) => {
      console.log(node.classList);
      node.classList.remove("hide");
    });

    const maleProfiles = document.querySelectorAll(".male");
    maleProfiles.forEach((node) => {
      node.classList.remove("hide");
    });
  }
}
